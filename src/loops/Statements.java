package loops;

public class Statements {

    // for, for each

    public static void main(String args[]) {

        int x = 30;

        switch (x) {
            case 2 :
                System.out.println("value 2");
                break;
            default:
                System.out.println("nothing");
                break;
            case 10:
                System.out.println("value 10");
                break;
            case 25:
                System.out.println("value 30");
                break;
        }
    }
}
